x = 5
y = 3

#Penjumlahan
print("penjumlahan =", x + y)

# Pengurangan (-)
print("pengurangan =", x - y)

# perkalian (*)
print("perkalian =", x * y)

# pembagian (/)
print("pembagian =", x / y)

# modulus (%)
print("modulus =", x % y)

# perpangkatan (**)
print("perpangkatan =", x ** y)

# pembagian bulat (//)
print("pembagian bulat =", x // y)
